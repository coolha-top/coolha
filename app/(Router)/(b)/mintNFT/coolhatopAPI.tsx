'use client'
import { useEffect, useState } from 'react';


export function TokenURL() {
    const [TokenURLdata, setTokenURLdata] = useState<any>(null); // 用于存储元数据
    const [error, setError] = useState<string | null>(null); // 用于存储错误信息

    useEffect(() => {
        async function fetchData() {
            try {
                const response = await fetch('/nft-metadata.json');
                if (!response.ok) {
                    throw new Error(`HTTP error! status: ${response.status}`);
                }
                const data = await response.json();
                setTokenURLdata(data);
            } catch (err) {
                setError((err as Error).message); // 捕获错误信息
            }
        }

        fetchData();
    }, []);
	return <>
            <div>名称: {TokenURLdata.name}</div>
            <div>描述: {TokenURLdata.description}</div>
            <div>
                <img
                    src={`https://gateway.pinata.cloud/${TokenURLdata.image}`}
                    alt="NFT Image"
                />
            </div>
            <div>属性:</div>
            <ul>
                {TokenURLdata.attributes.map((attr: any, index: number) => (
                    <li key={index}>
                        {attr.trait_type}: {attr.value}
                    </li>
                ))}
            </ul>
	</>
}
